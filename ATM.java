    private void btndepositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndepositActionPerformed
       //it is used to print the result and the message after successful and unsuccessful completion of withdrawl
        try{
            double amount=Double.parseDouble(txtamt.getText());
            if(atm.deposit(amount)){
                lblresult.setText("Amount Deposited Successfully!!!!");
                lblmessage.setText("Thank You for using the ATM !!!");
                removeComponents();
            }
            else{
                lblresult.setText("Invalid Amount");
                lblresult.setForeground(Color.red);
                txtamt.setText("");
            }
        }
        catch(NumberFormatException e){
            lblresult.setText("Please enter a valid amount");
            lblresult.setForeground(Color.red);
        }
    }//GEN-LAST:event_btndepositActionPerformed

