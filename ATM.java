    private void btnwithdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnwithdrawActionPerformed
        //it is used to print the result and the message after successful and unsuccessful completion of depositing
        try{
            double amount=Double.parseDouble(txtamt.getText());
            if(atm.withdraw(amount)){
                lblresult.setText("Amount Withdrawn Successfully!!!!");
                lblmessage.setText("Thank You for using the ATM !!!");
                removeComponents();
            }
            else{
                lblresult.setText("Insufficient Balance or Invalid Amount");
                lblresult.setForeground(Color.red);
                txtamt.setText("");
            } 
        }
        catch(Exception e){
            lblresult.setText("Please enter a valid amount");
            lblresult.setForeground(Color.red);
            txtamt.setText("");
        }
    }//GEN-LAST:event_btnwithdrawActionPerformed

